    public void listarVuelos(){
        try {
            String name = barra_busqueda.getText();
            DAOVuelos dao = new DAOVuelosIMPL();
            DefaultTableModel model = (DefaultTableModel) tabla_vuelos.getModel();
            model.setRowCount(0);
            dao.listarVU(name).forEach((v)->model.addRow(new Object[]{v.getIdvuelo(),v.getCodigo(),v.getEstado(),
                v.getOrigen(),v.getDestino(), v.getHorasalida(), v.getHorallegada(), v.getCantidadpasajeros(), 
                v.getTripulacion() != null ? v.getTripulacion().getIdtripulacion() : "N/A", v.getAvion() != null? v.getAvion().getIdavion(): "N/A", v.getFecha()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
