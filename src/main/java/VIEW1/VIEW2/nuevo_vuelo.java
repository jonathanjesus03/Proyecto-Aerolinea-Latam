    public void InitEdition(Vuelos vu){
        isEdition = true;
        vueloEdition = vu;
        tittle.setText("Editar Vuelo");
        btnRegistrar.setText("ACTUALIZAR");
        txtcodigo.setText(vueloEdition.getCodigo());
        txtdestino.setText(vueloEdition.getDestino());
        txtfecha.setText(vueloEdition.getFecha());
        txtestado.setText(vueloEdition.getEstado());
        txtorigen.setText(vueloEdition.getOrigen());
        txthoraLlegada.setText(vueloEdition.getHorallegada());
        txthoraSalida.setText(vueloEdition.getHorasalida());
    }
    
